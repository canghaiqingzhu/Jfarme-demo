create database xie;

use xie;

create table user
(
 id int auto_increment primary key,
 username varchar(20),
 password varchar(20),
 root int
);

create table thingmassage
(
 id int auto_increment primary key,
 thingId varchar(20),
 thingName varchar(20),
 shangjia varchar(20),
 jinjia varchar(20),
 shoujia varchar(20)
);

create table xiaodan
(
	id int auto_increment primary key,
	thingId varchar(20),
	number varchar(20),
	time varchar(20),
 	zhiyuan varchar(20)
);

create table jindan
(
	id int auto_increment primary key,
	thingId varchar(20),
	number varchar(20),
	time varchar(20),
 	zhiyuan varchar(20)
);

create table kucun
(
	Id int auto_increment primary key,
	thingId varchar(20),
	number varchar(20)
);

